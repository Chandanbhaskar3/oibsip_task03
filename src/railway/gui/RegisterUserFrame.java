    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateInput() == false){
            JOptionPane.showMessageDialog(null,"Please input all data properly","Error!!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String result = matchPassword();
        if(result != null){
             JOptionPane.showMessageDialog(null,result);
             return;
        } 
        try{
            RegisterUserPojo user = new RegisterUserPojo();
            
            user.setLoginId(id);
            user.setMobileNo(txtMob.getText().trim());
            user.setUserName(txtName.getText().trim());
            user.setPassword(PasswordEncryption.passwordEncrypt(password1));
             boolean result1 = RegisterUserDao.registerNewUser(user);
             if(result1 ){
                JOptionPane.showMessageDialog(null,"User registered successfully","Successfull!!",JOptionPane.INFORMATION_MESSAGE); 
                 getNewId();
                return;
             }
             JOptionPane.showMessageDialog(null,"Registration unsuccessfull","Can't register!!",JOptionPane.ERROR_MESSAGE);
             
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,"User already registered");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

