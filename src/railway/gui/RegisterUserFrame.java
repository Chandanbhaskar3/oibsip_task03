    private boolean validateInput() {
       txtLogIn.getText().trim();
       char [] p1 = jPassword1.getPassword();
       char [] p2 = jPassword2.getPassword();
       if(txtLogIn.getText().trim().isEmpty() || p1.length == 0 || p2.length == 0 || txtName.getText().trim().isEmpty())
           return false;
       password1 = new String(p1).trim();
       password2 = new String(p2).trim();
       if(!contactNoValid()){
           JOptionPane.showMessageDialog(null," Inavalid Contact Number" );
           return false;
       }
       return true;
    }

    private String matchPassword() {
       if(password1.length() < 3 || password2.length() < 3 )
          return "Password must be greater than 3 characters";
       if(password1.equals(password2) == false)
           return "Password doesn't match";
       return null;
           
       
    }
    private boolean contactNoValid() {
         char [] mob = txtMob.getText().trim().toCharArray();
         for(char ch:mob){
             if(Character.isDigit(ch) == false){
                 return false;
             }
         }
             if(txtMob.getText().trim().length() == 10)
                 return true;
                return false;
        
    }
     private void getNewId() {
        try {
             id = RegisterUserDao.getNewUserId();
            txtLogIn.setText(id);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB addReceptionistFrame");
            ex.printStackTrace();
        }
    }
